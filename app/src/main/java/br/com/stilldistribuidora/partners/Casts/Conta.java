package br.com.stilldistribuidora.partners.Casts;

public class Conta {

	private String type;
	private String name_completed;
	private String cpf;
	private String name_bank;
	private String agency;
	private String conta;
	private String type_pix;
	private String pix;


	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName_completed() {
		return name_completed;
	}

	public void setName_completed(String name_completed) {
		this.name_completed = name_completed;
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	public String getName_bank() {
		return name_bank;
	}

	public void setName_bank(String name_bank) {
		this.name_bank = name_bank;
	}

	public String getAgency() {
		return agency;
	}

	public void setAgency(String agency) {
		this.agency = agency;
	}

	public String getConta() {
		return conta;
	}

	public void setConta(String conta) {
		this.conta = conta;
	}

	public String getType_pix() {
		return type_pix;
	}

	public void setType_pix(String type_pix) {
		this.type_pix = type_pix;
	}

	public String getPix() {
		return pix;
	}

	public void setPix(String pix) {
		this.pix = pix;
	}
}
